package com.app.kyc.model;

public class Page
{
   private int page;
   private int perPage;

   public int getPage()
   {
      return page;
   }

   public void setPage(int page)
   {
      this.page = page;
   }

   public int getPerPage()
   {
      return perPage;
   }

   public void setPerPage(int perPage)
   {
      this.perPage = perPage;
   }
}
